//If else + if else

let nota = -1;

if (nota === 5) {
  console.log("Exelente calificacion");
} else if (nota === 4) {
  console.log("Buena calificacion");
} else if (nota === 3) {
  console.log("Regular Calificacion");
} else if (nota === 2) {
  console.log("Baja calificacion");
} else if (nota === 1) {
  console.log("Mala calificacion");
} else if (nota === 0) {
  console.log("Orrible calificacion");
} else {
  console.log("Error nota no definida, disponible 1 - 5");
}
