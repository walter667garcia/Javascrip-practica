//Sentencia Switch

let nota = 50;

switch (nota) {
  case 5:
    console.log("Exelente calificacion");
    break;

  case 4:
    console.log("Buena calificacion");
    break;

  case 3:
    console.log("Regular calificaion");
    break;

  case 2:
    console.log("Mala calificacion");
    break;

  case 1:
    console.log("Estudia la proxima");
    break;

  default:
    console.log("Error ingresa una nota, del 1 ... 5");
    break;
}
